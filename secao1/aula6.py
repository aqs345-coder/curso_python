'''conversão de tipos 
(coerção, typecasting, type convertion, etc...)'''
print(1 + 1)
print('c' + 'b')
print(float(4) + 1)
print(type(float(4) + 1))
print(int(1.0) + 1)
print(type(int(1.0) + 1))
print(str(12) + 'c')
print(type(str(12) + 'c'))
print(bool('')) #Vazio = False
print(bool(' ')) #Não vazio = True
